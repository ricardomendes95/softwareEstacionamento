package com.softpark.impressaoJasper.model;

public class EntradaPrint {

    String nomeEmpresa, cnpj,endereco, telefone, dataEntrada, horaEntrada, placa, valPrimeiraHora;

    public String getNomeEmpresa() {
        return nomeEmpresa;
    }


    public void setNomeEmpresa(String nomeEmpresa) {
        this.nomeEmpresa = nomeEmpresa;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getDataEntrada() {
        return dataEntrada;
    }

    public void setDataEntrada(String dataEntrada) {
        this.dataEntrada = dataEntrada;
    }

    public String getHoraEntrada() {
        return horaEntrada;
    }

    public void setHoraEntrada(String horaEntrada) {
        this.horaEntrada = horaEntrada;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getValPrimeiraHora() {
        return valPrimeiraHora;
    }

    public void setValPrimeiraHora(String valPrimeiraHora) {
        this.valPrimeiraHora = valPrimeiraHora;
    }
}
